public class assignmentOperators {
    public static void main(String[] args) {
        int a=5,b=3;
        System.out.println("A : "+a);
        System.out.println("B : "+b);
        System.out.println();
        a+=b;
        System.out.println("a+=b");
        System.out.println("A : "+a);
        System.out.println("B : "+b);
        System.out.println();
        a-=b;
        System.out.println("a-=b");
        System.out.println("A : "+a);
        System.out.println("B : "+b);
        System.out.println();
        a*=b;
        System.out.println("a*=b");
        System.out.println("A : "+a);
        System.out.println("B : "+b);
        System.out.println();
        a/=b;
        System.out.println("a/=b");
        System.out.println("A : "+a);
        System.out.println("B : "+b);
        System.out.println();
        a=b;
        System.out.println("a=b");
        System.out.println("A : "+a);
        System.out.println("B : "+b);

    }
}
